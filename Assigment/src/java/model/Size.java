package model;

/**
 *
 * @author Admin
 */
public class Size {
    private int sid;
    private String name;

    public Size() {
    }

    public Size(int sid, String name) {
        this.sid = sid;
        this.name = name;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
